/*
** f(6) = f(5)+f(4) = 5+3 = 8
** f(5) = f(4)+f(3) = 3+2 = 5
** f(4) = f(3)+f(2) = 2+1 = 3
** f(3) = f(2)+f(1) = 1+1 = 2
** f(2) = f(1)+f(0) = 1+0 = 1
** f(1) = 1
** f(0) = 0
*/

int	ft_fibonacci(int index)
{
	if (index == 0)
		return (0);
	else if (index == 1)
		return (1);
	else
		return (ft_fibonacci(index - 1) + ft_fibonacci(index - 2));
}
